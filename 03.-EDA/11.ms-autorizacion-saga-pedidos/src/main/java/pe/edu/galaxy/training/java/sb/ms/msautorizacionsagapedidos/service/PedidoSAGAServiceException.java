package pe.edu.galaxy.training.java.sb.ms.msautorizacionsagapedidos.service;

public class PedidoSAGAServiceException extends Exception {

	private static final long serialVersionUID = 4576709010939604694L;

	public PedidoSAGAServiceException() {
		
	}

	public PedidoSAGAServiceException(String message) {
		super(message);
		
	}

	public PedidoSAGAServiceException(Throwable cause) {
		super(cause);
		
	}

	public PedidoSAGAServiceException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public PedidoSAGAServiceException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

}
